#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*a;

	a = (unsigned char *)s;
	if (n == 0)
		return ;
	while (n)
	{
		n--;
		*a = 0;
		a++;
	}
}
